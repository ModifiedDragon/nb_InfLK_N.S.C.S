    private void ctfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ctfActionPerformed
        double celsius = Double.parseDouble(this.textCelsius.getText());
        double fahrenheit = (celsius * 9) / 5 + 32;
        this.textFahrenheit.setText("" + fahrenheit);
    }//GEN-LAST:event_ctfActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double fahrenheit = Double.parseDouble(this.textFahrenheit.getText());
        double celsius = ((fahrenheit - 32) * 5 / 9);
        this.textCelsius.setText("" + celsius);
    }//GEN-LAST:event_jButton1ActionPerformed

