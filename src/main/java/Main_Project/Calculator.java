    private void textCelsiusKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_textCelsiusKeyTyped
        char key = evt.getKeyChar();
        if (Character.isDigit(key)) {
            Error.setText("");
        } else {
            Error.setText("Please write a Number");
            evt.consume();
        }
    }//GEN-LAST:event_textCelsiusKeyTyped

